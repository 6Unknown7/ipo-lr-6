# Импортируем модуль random для работы со случайными числами
import random

# Генерируем случайное количество строк от 4 до 8
rows = random.randint(4,8)

# Генерируем случайное количество столбцов от 4 до 8
cols = random.randint(4,8)

# Создаем список значений, которые будем использовать для заполнения матрицы
list = [-3, -5, -2, -12, 0, 15, 4, 7, 2]

# Создаем пустой список для будущей матрицы
matrix = []

# Начинаем внешний цикл для создания строк
for i in range(rows):
    # Создаем пустой список для текущей строки
    row = []
    
    # Внутренний цикл для заполнения текущей строки столбцами
    for k in range(cols):
        # Добавляем случайный элемент из списка в текущую строку
        row.append(random.choice(list))
    
    # Добавляем готовую строку в матрицу
    matrix.append(row)

# Выводим заголовок для матрицы
print("Матрица: ")

# Цикл для красивого вывода матрицы
for row in matrix:
    # Форматируем каждую строку: выравниваем числа по 3 символа и соединяем пробелами
    formatted_row = " ".join(f"{num:3}" for num in row)
    # Выводим отформатированную строку
    print(formatted_row)

# Вычисляем сумму всех четных элементов матрицы
# Генератор проходит по всем строкам и всем элементам в них
# и суммирует только те элементы, которые делятся на 2 без остатка (четные)
summ = sum(element for row in matrix for element in row if element % 2 == 0)

# Выводим результат суммы четных элементов
print(f"\nСумма всех четных элементов: {summ}")